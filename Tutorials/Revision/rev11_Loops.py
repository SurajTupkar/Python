"""
Loops :
-> Loops are used to repeat instructions.
-> Two Types :
                1) while
                2) for

1) while 
-> A while loop runs again and again as long as a condition is True.
-> It stops when the condition becomes False

Syntax :

while condition:
    code

> First python check the condition
> If it is True -> code runs
> After running -> condition is checked again
> If False -> loop stops
                
"""
i=0
while(i<=5):
    print("Hello World")
    i+=1

i=1
while(i<=5):
    print(i)
    i+=1


